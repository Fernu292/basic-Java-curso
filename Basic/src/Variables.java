public class Variables {
    public static void main(String[] args){
        //Enteros
        int speed;
        //Asignar valor a una variable, con dato nombre = valor
        speed = 10;
        System.out.println(speed);

        //Strings
        int salary = 1000;
        String employerName = "Fernando";

        System.out.println(salary+employerName);
    }
}
