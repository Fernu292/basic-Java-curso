public class DataTypes {
    public static void main(String[] args){

        //Int con rango de 10 numeros 4 bytes
        int n = 1234567890;

        //Byte entre -128 a 127 1 byte
        byte nB = 12;

        //Short 2 bytes
        short nS = 12293;

        //long son 8 bytes, necesita colocar una L al final de la variable
        long nL = 1928200283L;


        //Tipos flotantes 4 bytes

        //El tipo float necesita colocar una F al final
        float nF = 1.145F;

        double nD = 123.283681928;

        //Tipo de dato caracter char
        //char 2 bytes
        char letra = 'w';
        String Nombre = "Luis";

        //Datos logicos 2 bytes

        boolean verdadero = true;
        boolean falso = false;

        //Variables dinamicas a partir de Java 10

        var nombre = "Luis";


        final var nombreNuevo = "Luis Fernadno";
    }
}
